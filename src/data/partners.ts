// Generate reviews for products
export const generateProductReviews = (productId: string, count: number) => {
  return Array.from({ length: count }, (_, i) => ({
    id: `review-${productId}-${i + 1}`,
    productId,
    userId: faker.string.uuid(),
    userName: faker.person.fullName(),
    isVerifiedPurchase: faker.datatype.boolean(0.8), // 80% are verified purchases
    rating: faker.number.int({ min: 3, max: 5 }), // Skewed toward positive reviews
    title: faker.lorem.sentence({ min: 3, max: 8 }),
    content: faker.lorem.paragraphs({ min: 1, max: 3 }),
    pros: faker.helpers.multiple(() => faker.lorem.sentence(), { count: faker.number.int({ min: 1, max: 3 }) }),
    cons: faker.helpers.multiple(() => faker.lorem.sentence(), { count: faker.number.int({ min: 0, max: 2 }) }),
    helpfulVotes: faker.number.int({ min: 0, max: 50 }),
    createdAt: faker.date.recent({ days: 90 }).toISOString(),
    updatedAt: faker.date.recent({ days: 30 }).toISOString(),
    childAge: faker.helpers.arrayElement(['Newborn', '0-6 months', '6-12 months', '1-2 years', '2-3 years', '3-5 years']),
    isHighlighted: i < 2, // First 2 reviews are highlighted
    isEdited: faker.datatype.boolean(0.2), // 20% are edited
  }));
};